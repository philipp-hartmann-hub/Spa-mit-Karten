export function continentOf(countryId: string): Exclude<ContinentId, 'all'> | null {
  return COUNTRY_CONTINENT[countryId] ?? null
}

export function matchesContinent(countryId: string, filter: ContinentFilter): boolean {
  if (filter === 'all') return true
  return continentOf(countryId) === filter
}
